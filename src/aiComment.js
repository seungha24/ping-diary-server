// 페르소나가 지정되지 않았을 때의 기본(다정한 친구)
const DEFAULT_PROMPT = `[페르소나: 친구]
- 관점: 곁에 있는 친한 친구. 편들어주고 같이 반응해준다.
- 하는 일: 일기 속 구체적 사실 하나에 진심으로 반응하고, 공감 위주로 짧게 답한다. 필요하면 가벼운 질문 하나.
- 톤: 편한 반말. 과장 없이 진심으로.
- 하지 말 것: 훈수, 판단, 뻔한 위로.`;

/**
 * 일기 정보로 프롬프트에 넣을 사용자 메시지를 구성한다.
 * @param {string} content 일기 본문
 * @param {{title?: string, tags?: string[], mood?: string, recent?: string}} meta 부가 정보
 * @returns {string} 모델에 전달할 [일기] 블록
 */
function buildUserMessage(content, meta = {}) {
  const { title, tags, mood, recent } = meta;
  const tagStr = Array.isArray(tags) ? tags.join(', ') : (tags || '');
  const lines = ['[일기]'];
  lines.push(`제목: ${title && title.trim() ? title.trim() : '(없음)'}`);
  lines.push(`내용: ${content}`);
  lines.push(`태그: ${tagStr || '(없음)'}`);
  if (mood && String(mood).trim()) lines.push(`기분: ${mood}`);
  if (recent && String(recent).trim()) lines.push(`최근 기록 맥락: ${recent}`);
  return lines.join('\n');
}

/**
 * 일기 내용과 페르소나로 AI 코멘트를 생성한다.
 * @param {string} content 일기 본문
 * @param {string} persona 페르소나(선생님/엄마/상담사/미래의 나 등)
 * @param {{title?: string, tags?: string[], mood?: string, recent?: string}} [meta] 부가 정보(제목·태그·기분·최근기록)
 * @returns {Promise<string>} 생성된 코멘트
 */
async function generateComment(content, persona, meta = {}) {
  const personaPrompt = PERSONA_PROMPTS[persona] || DEFAULT_PROMPT;
  const systemPrompt = `${COMMON_RULES}\n\n${personaPrompt}`;
  const completion = await openai.chat.completions.create({
    model: 'gpt-4o-mini',
    temperature: 0.85,
    max_tokens: 320,
    messages: [
      { role: 'system', content: systemPrompt },
      { role: 'user', content: buildUserMessage(content, meta) },
    ],
  });
  return completion.choices[0].message.content.trim();
}

module.exports = { generateComment, buildUserMessage, PERSONA_PROMPTS, DEFAULT_PROMPT, COMMON_RULES };
