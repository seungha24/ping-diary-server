/**
 * 일기 정보로 프롬프트에 넣을 사용자 메시지를 구성한다.
 * @param {string} content 일기 본문
 * @param {{title?: string, tags?: string[], mood?: string, recent?: string}} meta 부가 정보
 * @returns {string} 모델에 전달할 [일기] 블록
 */
// 본문 마커(질문·사진)는 AI에게 노이즈라 제거
function stripPhotoMarkers(text) {
  return String(text || '')
    .replace(/^\[q:[^\]]+\]\s*/, '')
    .replace(/\[photo:[^\]\s]+\]/g, '')
    .replace(/\n{3,}/g, '\n\n')
    .trim();
}

// 본문 맨 앞 [q:질문] 마커에서 '오늘의 질문' 추출
function extractQuestion(text) {
  const m = String(text || '').match(/^\[q:([^\]]+)\]\s*/);
  return m ? m[1] : null;
}

function buildUserMessage(content, meta = {}) {
  const { title, tags, mood, recent } = meta;
  const tagStr = Array.isArray(tags) ? tags.join(', ') : (tags || '');
  const lines = ['[일기]'];
  lines.push(`제목: ${title && title.trim() ? title.trim() : '(없음)'}`);
  const question = extractQuestion(content);
  if (question) lines.push(`오늘의 질문: ${question} (사용자가 이 질문에 답하며 쓴 일기 — 답한 내용에 반응해줘)`);
  lines.push(`내용: ${stripPhotoMarkers(content)}`);
  lines.push(`태그: ${tagStr || '(없음)'}`);
  if (mood && String(mood).trim()) lines.push(`기분: ${mood}`);
  if (recent && String(recent).trim()) lines.push(`최근 기록 맥락: ${recent}`);
  return lines.join('\n');
}

/**
 * 일기 내용과 페르소나로 AI 코멘트를 생성한다.
 * @param {string} content 일기 본문
 * @param {string} persona 페르소나(선생님/엄마/상담사/미래의 나 등)
 * @param {{title?: string, tags?: string[], mood?: string, recent?: string}} [meta] 부가 정보(제목·태그·기분·최근기록)
 * @returns {Promise<string>} 생성된 코멘트
 */
// 생성된 코멘트가 부적절하지 않은지 OpenAI Moderation API로 검사한다.
// flagged면 true를 돌려주고, 검사 자체가 실패하면(네트워크 등) 서비스를 막지
// 않기 위해 false(통과)로 처리한다.
async function isFlagged(text) {
  try {
    const res = await getOpenAI().moderations.create({
      model: 'omni-moderation-latest',
      input: text,
    });
    return Boolean(res.results?.[0]?.flagged);
  } catch (e) {
    console.error('[MODERATION] 검사 실패, 통과 처리:', e.message);
    return false;
  }
}

// Moderation에 걸렸을 때 대신 내보내는 안전한 기본 코멘트
const SAFE_FALLBACK = '오늘 하루의 기록을 잘 읽었어요. 마음속에 담아둔 이야기를 이렇게 적어낸 것만으로도 충분히 의미 있는 시간이었을 거예요.';

async function generateComment(content, persona, meta = {}) {
  const personaPrompt = PERSONA_PROMPTS[persona] || DEFAULT_PROMPT;
  const systemPrompt = `${COMMON_RULES}\n\n${personaPrompt}`;
  const completion = await getOpenAI().chat.completions.create({
    model: 'gpt-4o-mini',
    temperature: 0.85,
    max_tokens: 320,
    messages: [
      { role: 'system', content: systemPrompt },
      { role: 'user', content: buildUserMessage(content, meta) },
    ],
  });
  const text = completion.choices[0].message.content.trim();
  // 생성 결과 안전성 검사: 부적절하면 안전한 기본 코멘트로 대체
  if (await isFlagged(text)) {
    console.warn('[MODERATION] 부적절 코멘트 차단 → 기본 코멘트 대체');
    return SAFE_FALLBACK;
  }
  return text;
}

